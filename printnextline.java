public class printnextline{
    public static void main(String[]args){
        //three ways of printing output
        //first by using print
        System.out.print("Hello World With Java"); 
        //second by using println we can print in the next line      
        System.out.println("Hello World With Java");
        //third by using \n in side double "" we can print in the next line
        System.out.print("Hello World With Java\n");



    }
}